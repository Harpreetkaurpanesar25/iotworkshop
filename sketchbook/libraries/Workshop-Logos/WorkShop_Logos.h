void drawOracleLogo(Adafruit_SSD1306 display) {
  // Clear the buffer.
  display.clearDisplay();
  
  // miniature bitmap display
  display.drawBitmap(0, 0,  ORACLE_LOGO_BMP, 64, 48, 1);
  display.display();
  delay(2000);
}

void drawNoWifiLogo(Adafruit_SSD1306 display) {
  // Clear the buffer.
  display.clearDisplay();
  
  // miniature bitmap display
  display.drawBitmap(0, 0,  NO_WIFI_LOGO, 64, 48, 1);
  display.display();
}

void drawNoWifiLogoOff(Adafruit_SSD1306 display) {
  // Clear the buffer.
  display.clearDisplay();
  
  // miniature bitmap display
  display.drawBitmap(0, 0,  NO_WIFI_LOGO_OFF, 64, 48, 1);
  display.display();
}

bool drawWifiLogoToggle(Adafruit_SSD1306 display, bool wifiLogoOn){
  if (wifiLogoOn) {
    drawNoWifiLogo(display);
  } else {
    drawNoWifiLogoOff(display);
  }
  return !wifiLogoOn;
}

